import { spawnSync } from 'node:child_process';
// Regenerates predict-prescribed-observers.vectors.json — the cross-language
// parity fixture for `predictPrescribedObservers`. Each vector's `expected`
// field is produced by the Rust reference example
//   ar-io-solana-contracts/programs/ario-gar/examples/predict_prescribed_observers.rs
// which faithfully replicates the on-chain `prescribe_epoch` selection. The
// SDK test asserts the TypeScript helper reproduces these outputs byte-for-byte.
//
// Usage:
//   # Option A — use a prebuilt example binary (no cargo invocation):
//   PREDICT_BIN=/path/to/predict_prescribed_observers \
//     node src/solana/__fixtures__/generate-prescribe-vectors.mjs
//
//   # Option B — build+run via cargo in the sibling contracts repo:
//   ARIO_CONTRACTS_DIR=../ar-io-solana-contracts \
//     node src/solana/__fixtures__/generate-prescribe-vectors.mjs
//
// Commit the regenerated JSON alongside any change to the selection algorithm
// in BOTH repos.
import { createHash } from 'node:crypto';
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import { getAddressDecoder } from '@solana/kit';

const here = dirname(fileURLToPath(import.meta.url));
const addressDecoder = getAddressDecoder();

/** Deterministic, valid base58 pubkey from a small integer tag. */
function pubkey(tag) {
  const u = new Uint8Array(32);
  u[0] = tag & 0xff;
  u[1] = (tag >> 8) & 0xff;
  u[31] = 0x2a;
  return addressDecoder.decode(u).toString();
}

/** 32-byte hashchain derived from a label (just needs to be deterministic). */
function hashchain(label) {
  return createHash('sha256').update(label).digest('hex');
}

/** Build `count` slots; weightFn(i) -> bigint weight. */
function slots(count, weightFn) {
  return Array.from({ length: count }, (_, i) => ({
    address: pubkey(i + 1),
    compositeWeight: weightFn(i).toString(),
  }));
}

const scenarios = [
  {
    name: 'distinct-weights-K-lt-active',
    hashchain: hashchain('distinct'),
    maxObservers: 3,
    slots: slots(5, (i) => [100n, 50n, 25n, 10n, 5n][i]),
  },
  {
    name: 'equal-weights-retry-path',
    hashchain: hashchain('equal'),
    maxObservers: 5,
    slots: slots(8, () => 100n),
  },
  {
    name: 'interleaved-zero-weight-slots',
    hashchain: hashchain('zeros'),
    maxObservers: 3,
    slots: slots(6, (i) => [0n, 100n, 0n, 50n, 0n, 25n][i]),
  },
  {
    name: 'K-ge-active-selects-all-nonzero',
    hashchain: hashchain('all'),
    maxObservers: 10,
    slots: slots(4, (i) => [10n, 20n, 30n, 40n][i]),
  },
  {
    name: 'large-registry-caps-at-K',
    hashchain: hashchain('large'),
    maxObservers: 10,
    slots: slots(100, (i) => BigInt(1 + ((i * 7) % 50))),
  },
  {
    name: 'dominant-slot-antidup-stress',
    hashchain: hashchain('dominant'),
    maxObservers: 2,
    slots: slots(3, (i) => [1_000_000n, 1n, 1n][i]),
  },
  {
    name: 'single-slot',
    hashchain: hashchain('single'),
    maxObservers: 1,
    slots: slots(1, () => 100n),
  },
  {
    name: 'zero-total-weight',
    hashchain: hashchain('zerototal'),
    maxObservers: 2,
    slots: slots(3, () => 0n),
  },
];

function runRust(vector) {
  const stdin =
    [
      vector.hashchain,
      String(vector.maxObservers),
      ...vector.slots.map((s) => `${s.address} ${s.compositeWeight}`),
    ].join('\n') + '\n';

  const bin = process.env.PREDICT_BIN;
  const res = bin
    ? spawnSync(bin, [], { input: stdin, encoding: 'utf8' })
    : spawnSync(
        'cargo',
        [
          'run',
          '--quiet',
          '--manifest-path',
          join(
            process.env.ARIO_CONTRACTS_DIR ?? '../ar-io-solana-contracts',
            'Cargo.toml',
          ),
          '-p',
          'ario-gar',
          '--example',
          'predict_prescribed_observers',
        ],
        { input: stdin, encoding: 'utf8' },
      );
  if (res.status !== 0) {
    throw new Error(
      `Rust example failed for ${vector.name}: ${res.stderr || res.error}`,
    );
  }
  return res.stdout.split('\n').filter((l) => l.trim().length > 0);
}

const vectors = scenarios.map((v) => ({ ...v, expected: runRust(v) }));
const out = {
  _README:
    'Generated by generate-prescribe-vectors.mjs. `expected` comes from the ' +
    'Rust reference example (ario-gar/examples/predict_prescribed_observers.rs). ' +
    'Do not hand-edit; regenerate when the selection algorithm changes.',
  vectors,
};
writeFileSync(
  join(here, 'predict-prescribed-observers.vectors.json'),
  JSON.stringify(out, null, 2) + '\n',
);
console.log(`Wrote ${vectors.length} vectors.`);
for (const v of vectors) {
  console.log(`  ${v.name}: ${v.expected.length} selected`);
}
